    private void limpiarFormulario() {
        nombreTxt.setEnabled(false);
        nombreTxt.setEditable(false);
        nombreTxt.setText("");
        anioTxt.setEnabled(false);
        anioTxt.setText("");

    }
    
    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        try {
            distribuidor = new DistribuidorService().buscarDistribuidor(rutTxt.getText());
            if (distribuidor != null) {
                nombreTxt.setEnabled(true);
                nombreTxt.setText(distribuidor.getNombreEmpDist());
                
                anioTxt.setEnabled(true);
                anioTxt.setText(Integer.toString(distribuidor.getAnioInicioDist()));

            } else {
                limpiarFormulario();
                JOptionPane.showMessageDialog(rootPane,
                    "Distribuidor no encontrado", "Error",
                    JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,
                "Ingrese un Rut valido", "Error",
                JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_buscarBtnActionPerformed

