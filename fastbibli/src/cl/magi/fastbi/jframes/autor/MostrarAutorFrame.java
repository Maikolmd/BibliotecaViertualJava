    private void ActualizarAutorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarAutorBtnActionPerformed
        // TODO add your handling code here:
        
        autor.setNombreAutor(NomAutroTxt.getText().trim());
        autor.setApellidoPatAutor(ApePatAutorTxt.getText().trim());
        autor.setApellidoMatAutor(ApeMatAutorTxt.getText().trim());

        if (new AutorService().actualizarAutor(autor)) {
            JOptionPane.showMessageDialog(this, "Autor con código " + autor.getIdAutor()
                    + "actualizado correctamente", "Correcto", JOptionPane.INFORMATION_MESSAGE);
            cargarJtable();
        } else {
            JOptionPane.showMessageDialog(this, "Ocurrio un error", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_ActualizarAutorBtnActionPerformed

    private void EliminarAutorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarAutorBtnActionPerformed
        // TODO add your handling code here:
        List<Autor> autores = new AutorService().obtenerAutores();
        try {
            int id = Integer.parseInt(CboAutorEliminar.getSelectedItem().toString());
            boolean esta = false;
            for (Autor aut : autores) {
                if (aut.getIdAutor()== id) {
                    esta = true;
                    if (new AutorService().eliminatAutor(id)) {
                        JOptionPane.showMessageDialog(this, "El autor con ID: " + id
                                + "se elimino correctamente", "Borrado", JOptionPane.INFORMATION_MESSAGE);
                        cargarJtable();
                    } else {
                        JOptionPane.showMessageDialog(this, "Hubo un error al borrar el autor", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    break;
                }
            }
            if (!esta) {
                JOptionPane.showMessageDialog(this, "Autor no existente", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar una id valida", "Error de validación", JOptionPane.ERROR_MESSAGE);
        }
        IdAutorTxt.setText("");
    }//GEN-LAST:event_EliminarAutorBtnActionPerformed

