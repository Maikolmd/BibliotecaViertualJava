    private void limpiarFormulario() {
        this.IdAutorTxt.setText("");
        this.NomAutroTxt.setText("");
        this.ApePatAutorTxt.setText("");
        this.ApeMatAutorTxt.setText("");
    }
    
    private void cargarAutorCbx() {
        this.CboAutorEliminar.removeAllItems();
        AutorService autorService = new AutorService();
        List<Autor> autores = autorService.obtenerAutores();
        for (Autor autor : autores) {
            this.CboAutorEliminar.addItem(autor);
        }
    }
    
    private void cargarJtable () {
        DefaultTableModel modelo = new DefaultTableModel();
        List<Autor> autores = new AutorService().obtenerAutores();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre del autor");
        modelo.addColumn("Apellido paterno");
        modelo.addColumn("Apellido materno");
        Object [] fila = new Object[4];
        for (Autor aut : autores) {
            fila[0] = aut.getIdAutor();
            fila[1] = aut.getNombreAutor();
            fila[2] = aut.getApellidoPatAutor();
            fila[3] = aut.getApellidoMatAutor();
            modelo.addRow(fila);
        }
        jtblAutor.setModel(modelo);
    }
    
